#include "Scene.h"

void Scene::addObject(Object& object, RenderType type) {
	if (type == FORWARD) {
		m_objects_forward.emplace_back(&object);
	} else {
		m_objects_deferred.emplace_back(&object);
	}
}

void Scene::addLight(Light& light) {
	m_lights.emplace_back(&light);
}

void Scene::setSkybox(const std::vector<std::string>& tex) {
	const float vertices[] = {
		-1.0f,  1.0f, -1.0f,
		-1.0f, -1.0f, -1.0f,
		 1.0f, -1.0f, -1.0f,
		 1.0f, -1.0f, -1.0f,
		 1.0f,  1.0f, -1.0f,
		-1.0f,  1.0f, -1.0f,

		-1.0f, -1.0f,  1.0f,
		-1.0f, -1.0f, -1.0f,
		-1.0f,  1.0f, -1.0f,
		-1.0f,  1.0f, -1.0f,
		-1.0f,  1.0f,  1.0f,
		-1.0f, -1.0f,  1.0f,

		 1.0f, -1.0f, -1.0f,
		 1.0f, -1.0f,  1.0f,
		 1.0f,  1.0f,  1.0f,
	 	 1.0f,  1.0f,  1.0f,
	 	 1.0f,  1.0f, -1.0f,
		 1.0f, -1.0f, -1.0f,

		-1.0f, -1.0f,  1.0f,
		-1.0f,  1.0f,  1.0f,
		 1.0f,  1.0f,  1.0f,
		 1.0f,  1.0f,  1.0f,
		 1.0f, -1.0f,  1.0f,
		-1.0f, -1.0f,  1.0f,

		-1.0f,  1.0f, -1.0f,
		 1.0f,  1.0f, -1.0f,
		 1.0f,  1.0f,  1.0f,
		 1.0f,  1.0f,  1.0f,
		-1.0f,  1.0f,  1.0f,
		-1.0f,  1.0f, -1.0f,

		-1.0f, -1.0f, -1.0f,
		-1.0f, -1.0f,  1.0f,
		 1.0f, -1.0f, -1.0f,
		 1.0f, -1.0f, -1.0f,
		-1.0f, -1.0f,  1.0f,
		 1.0f, -1.0f,  1.0f
	};

	MeshDetails md = { 36, 0 };

	std::unique_ptr<Mesh> mesh = std::make_unique<Mesh>((void*)vertices, nullptr, VertexBufferLayout::DEFAULT_P(), md);
	mesh->setMaterial(DIFFUSE_MAP, tex);

	m_skybox = std::make_unique<Model>();
	m_skybox->addMesh(mesh);
}